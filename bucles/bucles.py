cont = 0
while (cont < 10):
    num = int(input("Ingrese un numero valido(no tiene que ser 0): "))
    if(num > 0):
        print("Su numero es valido")
        cont = cont + 1
    else:
        print("Su numero no es valido, vuelva a ingresar otro numero")

